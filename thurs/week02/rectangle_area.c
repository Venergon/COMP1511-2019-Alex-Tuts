// A program to calculate the area of a rectangle

#include <stdio.h>
#include <stdlib.h>

int main(void) {
    int length = 0;
    int width = 0;
    int area = 0;

    printf("Please enter rectangle length: ");
    scanf("%d", &length);
    printf("Please enter rectangle width: ");
    scanf("%d", &width);

    area = length * width;
    printf("A rectangle with length %d and width %d has Area = %d\n", length, width, area);
    return EXIT_SUCCESS;
}
